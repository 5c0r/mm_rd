

<h2> About the Project</h2>

<p>This is a project for a course at the University of Oslo (LOL NO). It is a simple blogging platform that allows you to post articles and comments. The platform is built with SvelteKit and TypeScript.</p>

<p>The project is open source and can be found on GitHub: <a href="https://github.com/CoderPush/mm-reader">https://github.com/CoderPush/mm-reader</a></p>

